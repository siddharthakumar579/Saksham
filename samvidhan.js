const langLabels = { en: 'EN', hi: 'हि', ta: 'த', te: 'తె' };

let currentLang = 'en';

function setLang(lang) {
  currentLang = lang;
  const t = translations[lang];

  // Update all [data-i18n] elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // Update lang label
  document.getElementById('langLabel').textContent = langLabels[lang];

  // Mark active option
  document.querySelectorAll('.lang-option').forEach(el => {
    el.classList.toggle('active', el.getAttribute('data-lang') === lang);
  });

  closeLangMenu();
}

function toggleLangMenu() {
  const btn = document.getElementById('langBtn');
  const dd = document.getElementById('langDropdown');
  btn.classList.toggle('open');
  dd.classList.toggle('open');
}

function closeLangMenu() {
  document.getElementById('langBtn').classList.remove('open');
  document.getElementById('langDropdown').classList.remove('open');
}

// Close dropdown on outside click
document.addEventListener('click', e => {
  if (!e.target.closest('.lang-switcher')) closeLangMenu();
});

